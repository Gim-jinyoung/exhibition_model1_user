package VO;

public class LocalVO {

	private String ex_loc;
	private int ex_num;
	public LocalVO() {
	}
	public LocalVO(String ex_loc, int ex_num) {
		this.ex_loc = ex_loc;
		this.ex_num = ex_num;
	}
	/**
	 * @return the ex_loc
	 */
	public String getEx_loc() {
		return ex_loc;
	}
	/**
	 * @param ex_loc the ex_loc to set
	 */
	public void setEx_loc(String ex_loc) {
		this.ex_loc = ex_loc;
	}
	/**
	 * @return the ex_num
	 */
	public int getEx_num() {
		return ex_num;
	}
	/**
	 * @param ex_num the ex_num to set
	 */
	public void setEx_num(int ex_num) {
		this.ex_num = ex_num;
	}
	
	
}
